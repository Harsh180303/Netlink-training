# Examples of assignment operators are: (=, +=, -=, *=, /=, //=, **=, %=)


x = 10
print("x =", x)

x = 10
x += 2
print("x += 2: ", x)

x = 10
x -= 3
print("x -= 3:", x)

x = 10
x *= 2
print("x *= 2:", x)

x = 10
x /= 4
print("x /= 4:", x)

x = 10
x //= 3
print("x //= 3:", x)

x = 10
x %= 4
print("x %= 4:", x)

x = 10
x **= 3
print("x **= 3:", x)
