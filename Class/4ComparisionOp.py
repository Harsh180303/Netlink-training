# Examples of comparison operators in python
# types of comparison operators are: (==, !=, >, >=, <, <=)

a = int(input("Enter an integer: "))
b = int(input("Enter another integer: "))

print("checking a = b?: ",a == b)
print("checking a != b?: ",a != b)
print("checking a > b?: ",a > b)
print("checking a < b?: ",a < b)
print("checking a >= b?: ",a >= b)
print("checking a <= b?: ",a <= b)