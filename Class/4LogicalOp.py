# Logical Operators are: and, or, not

# and operator
'''
age = 20
Indian = True

print(age > 18 and Indian == True)
'''

# or operator
'''

marks = 33
print(marks > 33 or marks == 33)

'''

# not operator
'''
is_raining = False
print(not is_raining)    # return True
print(is_raining)   # return False
'''